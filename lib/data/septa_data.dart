// Helper functions
List<TransitLineModel> findConnectingLines(String startStation, String endStation) {
  return septaLines.where(
    (line) => line.connectsStations(startStation, endStation)
  ).toList();
}

// Get all stations for a specific transportation type
// For SEPTA, all stations are Regional Rail
List<String> getStationsByTransportationType(String transportationType) {
  if (transportationType == 'Regional Rail') {
    return allSeptaStations;
  }
  return [];
}